public class Realtional {
    public static void main(String[] args) {
        int a = 6;
        int b = 5;
        // Equal Operator
        boolean result = (a == b);
        System.out.println(result);

        // Not equal
        result = (a != b);
        System.out.println(result);

        // Greater than
        result = (a > b);
        System.out.println(result);

        // Less than
        result = (a < b);
        System.out.println(result);

        // Less than or equal to
        result = (a <= b);
        System.out.println(result);

        // Greater than or equal to
        result = (a >= b);
        System.out.println(result);
    }
}

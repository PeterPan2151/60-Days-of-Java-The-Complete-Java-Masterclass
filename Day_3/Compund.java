public class Compund {
    public static void main(String[] args) {
        int a = 5;
        // Addition
        a += 3;
        System.out.println(a);

        // Subtraction
        a -= 3;
        System.out.println(a);

        // Multiplication
        a *= 3;
        System.out.println(a);

        // Division
        a /= 3;
        System.out.println(a);

        // Modulo
        a %= 3;
        System.out.println(a);
    }
}

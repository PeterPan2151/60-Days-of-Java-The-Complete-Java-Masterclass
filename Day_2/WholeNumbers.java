public class WholeNumbers {
    public static void main(String[] args) {
        // Byte, short, int, long
        // byte - 8 bit (2^8 = 256)
        byte age = 127;

        // short - 16 bit (2^16 = 65536)
        short year = 2025;

        // int - 32 bits (2^32 = 4,294,967,296)
        int populationMexico = 131_983_911;

        // long - 64 bits (2^64)
        long worldPopulation = 8_000_000_000L;

        System.out.println(age);
        System.out.println(year);
        System.out.println(populationMexico);
        System.out.println(worldPopulation);
    }
}
